using System;
using System.Collections.Generic;
using System.Collections.Specialized;
using System.Web;
using System.Web.UI;
using System.Web.UI.WebControls;
using Sun.Identity.Saml2;
using Sun.Identity.Saml2.Exceptions;
using Sun.Identity.Common;
using SAML.Common;

namespace TSS.MVC
{
    public partial class InitiateLogout : System.Web.UI.Page
    {
        protected void Page_Load(object sender, EventArgs e)
        {
            string[] myCookies = Request.Cookies.AllKeys;
            foreach (string cookie in myCookies)
            {
                Response.Cookies[cookie].Expires = DateTime.Now.AddDays(-1);
            }

            ServiceProviderUtility.LogoutAndRedirect(this.Request, this.Response, this.Context,
                SAML.Common.Config.ConfigFolder, SAML.Common.Config.Portal, true);
            Session.Clear();
            Session.Abandon();
        }
    }
}
